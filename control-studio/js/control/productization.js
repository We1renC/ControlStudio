/**
 * Export a designed controller to executable code in C, Python, or MATLAB.
 *
 * Supported controller shapes:
 *   PID   — { type:'pid', Kp, Ki, Kd, N?, Ts? }
 *   TF    — { type:'tf',  num:number[], den:number[], Ts? }
 *   SS    — { type:'ss',  A, B, C, D, Ts? }
 *   SF    — { type:'sf',  K:number[], Ts? }     (state-feedback gain row vector)
 *
 * For continuous controllers, a discrete step (Euler / bilinear) is generated
 * automatically if `opts.Ts` is provided.
 *
 * @param {object}  ctrl          Controller descriptor (see above).
 * @param {'c'|'python'|'matlab'} target  Target language.
 * @param {object}  [opts]
 * @param {number}  [opts.Ts]     Sample time override (s).
 * @param {string}  [opts.varName='ctrl']  Variable/function name in generated code.
 * @param {boolean} [opts.includeHeader=true]  Emit file-header comment.
 * @returns {{ code: string, language: string, warnings: string[] }}
 */
export function exportController(ctrl, target, opts = {}) {
  if (!ctrl || typeof ctrl !== 'object') throw new Error('ctrl must be an object');
  const lang = (target || '').toLowerCase();
  if (!['c', 'python', 'matlab'].includes(lang)) {
    throw new Error(`Unsupported target "${target}". Use 'c', 'python', or 'matlab'.`);
  }

  const varName   = opts.varName ?? 'ctrl';
  const Ts        = opts.Ts ?? ctrl.Ts ?? null;
  const header    = opts.includeHeader !== false;
  const warnings  = [];

  const type = (ctrl.type || '').toLowerCase();

  switch (type) {
    case 'pid': return _exportPID(ctrl, lang, { varName, Ts, header, warnings });
    case 'tf':  return _exportTF(ctrl, lang, { varName, Ts, header, warnings });
    case 'ss':  return _exportSS(ctrl, lang, { varName, Ts, header, warnings });
    case 'sf':  return _exportSF(ctrl, lang, { varName, Ts, header, warnings });
    default:
      throw new Error(`Unknown controller type "${ctrl.type}". Supported: pid, tf, ss, sf.`);
  }
}

// ── helpers ──────────────────────────────────────────────────────────────────

function _fmt(x, digits = 8) {
  if (!Number.isFinite(x)) return String(x);
  // Use fixed if magnitude reasonable, otherwise exponential
  const abs = Math.abs(x);
  if (abs === 0) return '0.0';
  if (abs < 1e-4 || abs >= 1e7) return x.toExponential(digits);
  return x.toPrecision(digits + 1).replace(/\.?0+$/, '').replace(/^(\d+)$/, '$1.0');
}

function _arr(arr, digits = 8) {
  return arr.map((v) => _fmt(v, digits)).join(', ');
}

function _mat2c(mat) {
  return mat.map((row) => row.map((v) => _fmt(v)).join(', ')).join(',\n    ');
}

function _fileHdr(lang, desc) {
  const cmt = lang === 'c' ? '//' : lang === 'matlab' ? '%' : '#';
  const ts  = new Date().toISOString().slice(0, 10);
  return `${cmt} Auto-generated by ControlStudio P33 — ${ts}\n${cmt} ${desc}\n\n`;
}

// ── PID ──────────────────────────────────────────────────────────────────────

function _exportPID(ctrl, lang, { varName, Ts, header, warnings }) {
  const { Kp = 1, Ki = 0, Kd = 0, N = 100 } = ctrl;
  const discrete = Ts !== null;

  let code = header ? _fileHdr(lang, `PID controller — Kp=${Kp} Ki=${Ki} Kd=${Kd} N=${N}`) : '';

  if (lang === 'c') {
    if (discrete) {
      // Bilinear (Tustin) discrete PID
      const Ts_ = Ts;
      const Ki2 = Ki * Ts_ / 2;
      const Kd2 = Kd * N / (1 + N * Ts_ / 2);
      code += `/* Discrete PID (Tustin, Ts=${Ts_}s) */\n`;
      code += `typedef struct {\n`;
      code += `  double prev_e, prev_u_i, prev_u_d, prev_e_d;\n`;
      code += `} ${varName}_state_t;\n\n`;
      code += `double ${varName}_step(${varName}_state_t *s, double e) {\n`;
      code += `  double u_p = ${_fmt(Kp)} * e;\n`;
      code += `  double u_i = s->prev_u_i + ${_fmt(Ki2)} * (e + s->prev_e);\n`;
      code += `  double b   = ${_fmt(2 * Kd2 / Ts_)};\n`;
      code += `  double u_d = b * (e - s->prev_e) - s->prev_u_d * ${_fmt(N * Ts_ / 2 - 1)};\n`;
      code += `  s->prev_e   = e;\n`;
      code += `  s->prev_u_i = u_i;\n`;
      code += `  s->prev_u_d = u_d;\n`;
      code += `  return u_p + u_i + u_d;\n`;
      code += `}\n`;
    } else {
      warnings.push('Continuous PID: C code uses Euler integration; provide opts.Ts for bilinear.');
      code += `/* Continuous PID (Euler approx.) — provide Ts for proper discretization */\n`;
      code += `typedef struct { double integral, prev_e; } ${varName}_state_t;\n\n`;
      code += `double ${varName}_step(${varName}_state_t *s, double e, double dt) {\n`;
      code += `  s->integral += e * dt;\n`;
      code += `  double deriv = (e - s->prev_e) / (dt + 1e-12);\n`;
      code += `  s->prev_e = e;\n`;
      code += `  return ${_fmt(Kp)}*e + ${_fmt(Ki)}*s->integral + ${_fmt(Kd)}*deriv;\n`;
      code += `}\n`;
    }
  } else if (lang === 'python') {
    code += `import numpy as np\n\n`;
    code += `class ${_pyName(varName)}:\n`;
    code += `    """PID: Kp=${Kp}, Ki=${Ki}, Kd=${Kd}, N=${N}"""\n`;
    if (discrete) {
      code += `    Ts = ${Ts}\n\n`;
      code += `    def __init__(self):\n`;
      code += `        self.integral = 0.0\n`;
      code += `        self.prev_e   = 0.0\n\n`;
      code += `    def step(self, e):\n`;
      code += `        self.integral += e * self.Ts\n`;
      code += `        deriv = (e - self.prev_e) / (self.Ts + 1e-12)\n`;
      code += `        self.prev_e = e\n`;
      code += `        return ${_fmt(Kp)}*e + ${_fmt(Ki)}*self.integral + ${_fmt(Kd)}*deriv\n`;
    } else {
      code += `\n    def __init__(self):\n`;
      code += `        self.integral = 0.0\n`;
      code += `        self.prev_e   = 0.0\n\n`;
      code += `    def step(self, e, dt):\n`;
      code += `        self.integral += e * dt\n`;
      code += `        deriv = (e - self.prev_e) / (dt + 1e-12)\n`;
      code += `        self.prev_e = e\n`;
      code += `        return ${_fmt(Kp)}*e + ${_fmt(Ki)}*self.integral + ${_fmt(Kd)}*deriv\n`;
    }
  } else {
    // matlab
    code += `function u = ${varName}_step(e, state)\n`;
    code += `% PID: Kp=${Kp}, Ki=${Ki}, Kd=${Kd}, N=${N}\n`;
    if (discrete) {
      code += `Ts = ${Ts};\n`;
      code += `u = ${_fmt(Kp)}*e + ${_fmt(Ki)}*Ts*state.integral + ${_fmt(Kd)}/Ts*(e - state.prev_e);\n`;
    } else {
      code += `u = ${_fmt(Kp)}*e + ${_fmt(Ki)}*state.integral + ${_fmt(Kd)}*state.deriv;\n`;
    }
    code += `end\n`;
  }

  return { code, language: lang, warnings };
}

// ── TF ───────────────────────────────────────────────────────────────────────

function _exportTF(ctrl, lang, { varName, Ts, header, warnings }) {
  const num = ctrl.num ?? [1];
  const den = ctrl.den ?? [1];
  const discrete = Ts !== null || ctrl.Ts != null;
  const Ts_ = Ts ?? ctrl.Ts;

  let code = header ? _fileHdr(lang, `TF: num=[${_arr(num)}] / den=[${_arr(den)}]${discrete ? ` Ts=${Ts_}` : ''}`) : '';

  if (lang === 'c') {
    // Direct Form II transposed
    const n = Math.max(num.length, den.length) - 1; // order
    code += `/* Transfer function — Direct Form II transposed */\n`;
    code += `#define ${varName.toUpperCase()}_ORDER ${n}\n`;
    code += `static const double ${varName}_num[] = {${_arr(_padLeft(num, n + 1))}};\n`;
    code += `static const double ${varName}_den[] = {${_arr(_padLeft(den, n + 1))}};\n\n`;
    code += `typedef struct { double w[${n + 1}]; } ${varName}_state_t;\n\n`;
    code += `double ${varName}_step(${varName}_state_t *s, double u) {\n`;
    code += `  double a0 = ${varName}_den[0];\n`;
    code += `  double y  = (${varName}_num[0]/a0)*u + s->w[0];\n`;
    code += `  for (int i = 0; i < ${n}; i++) {\n`;
    code += `    s->w[i] = (${varName}_num[i+1]/a0)*u - (${varName}_den[i+1]/a0)*y\n`;
    code += `              + (i+1 < ${n} ? s->w[i+1] : 0.0);\n`;
    code += `  }\n`;
    code += `  return y;\n`;
    code += `}\n`;
    if (!discrete) warnings.push('Continuous TF exported as-is; ensure pre-discretized coefficients for real-time use.');
  } else if (lang === 'python') {
    code += `import numpy as np\n`;
    code += `from scipy.signal import lfilter, tf2ss\n\n`;
    code += `# Transfer function coefficients\n`;
    code += `num_${varName} = np.array([${_arr(num)}])\n`;
    code += `den_${varName} = np.array([${_arr(den)}])\n\n`;
    if (discrete) {
      code += `# Discrete-time filter (Ts=${Ts_})\n`;
      code += `class ${_pyName(varName)}:\n`;
      code += `    def __init__(self):\n`;
      code += `        self._zi = np.zeros(max(len(num_${varName}), len(den_${varName})) - 1)\n\n`;
      code += `    def step(self, u_sample):\n`;
      code += `        y, self._zi = lfilter(num_${varName}, den_${varName},\n`;
      code += `                              [u_sample], zi=self._zi)\n`;
      code += `        return float(y[0])\n`;
    } else {
      code += `# Continuous TF — convert to state-space for simulation\n`;
      code += `A, B, C, D = tf2ss(num_${varName}, den_${varName})\n`;
    }
  } else {
    // matlab
    code += `% Transfer function\n`;
    if (discrete) {
      code += `${varName} = tf([${_arr(num)}], [${_arr(den)}], ${Ts_});\n`;
    } else {
      code += `s = tf('s');\n`;
      code += `${varName} = tf([${_arr(num)}], [${_arr(den)}]);\n`;
    }
  }

  return { code, language: lang, warnings };
}

// ── SS ───────────────────────────────────────────────────────────────────────

function _exportSS(ctrl, lang, { varName, Ts, header, warnings }) {
  const A = ctrl.A, B = ctrl.B, C = ctrl.C, D = ctrl.D;
  if (!A || !B || !C || !D) throw new Error('SS controller requires A, B, C, D matrices.');
  const n = A.length;
  const m = B[0]?.length ?? 1;
  const p = C.length;
  const discrete = Ts !== null || ctrl.Ts != null;
  const Ts_ = Ts ?? ctrl.Ts;

  let code = header ? _fileHdr(lang, `State-space: n=${n}, m=${m}, p=${p}${discrete ? ` Ts=${Ts_}` : ''}`) : '';

  if (lang === 'c') {
    code += `/* State-space controller — n=${n} states, m=${m} inputs, p=${p} outputs */\n`;
    code += `#define ${varName.toUpperCase()}_N ${n}\n`;
    code += `#define ${varName.toUpperCase()}_M ${m}\n`;
    code += `#define ${varName.toUpperCase()}_P ${p}\n\n`;
    code += `static const double ${varName}_A[${n}][${n}] = {\n    {${_mat2c(A)}}\n};\n`;
    code += `static const double ${varName}_B[${n}][${m}] = {\n    {${_mat2c(B)}}\n};\n`;
    code += `static const double ${varName}_C[${p}][${n}] = {\n    {${_mat2c(C)}}\n};\n`;
    code += `static const double ${varName}_D[${p}][${m}] = {\n    {${_mat2c(D)}}\n};\n\n`;
    code += `typedef struct { double x[${n}]; } ${varName}_state_t;\n\n`;
    code += `void ${varName}_step(${varName}_state_t *s, const double u[${m}], double y[${p}]) {\n`;
    code += `  double xn[${n}] = {0};\n`;
    code += `  /* y = C*x + D*u */\n`;
    code += `  for (int i = 0; i < ${p}; i++) {\n`;
    code += `    y[i] = 0;\n`;
    code += `    for (int j = 0; j < ${n}; j++) y[i] += ${varName}_C[i][j] * s->x[j];\n`;
    code += `    for (int j = 0; j < ${m}; j++) y[i] += ${varName}_D[i][j] * u[j];\n`;
    code += `  }\n`;
    code += `  /* x+ = A*x + B*u */\n`;
    code += `  for (int i = 0; i < ${n}; i++) {\n`;
    code += `    for (int j = 0; j < ${n}; j++) xn[i] += ${varName}_A[i][j] * s->x[j];\n`;
    code += `    for (int j = 0; j < ${m}; j++) xn[i] += ${varName}_B[i][j] * u[j];\n`;
    code += `  }\n`;
    code += `  for (int i = 0; i < ${n}; i++) s->x[i] = xn[i];\n`;
    code += `}\n`;
    if (!discrete) warnings.push('Continuous SS exported; embed in ODE solver or pre-discretize.');
  } else if (lang === 'python') {
    code += `import numpy as np\n\n`;
    code += `class ${_pyName(varName)}:\n`;
    code += `    """State-space: n=${n}, m=${m}, p=${p}${discrete ? ', discrete Ts=' + Ts_ : ''}"""\n`;
    code += `    A = np.array(${_mat2py(A)})\n`;
    code += `    B = np.array(${_mat2py(B)})\n`;
    code += `    C = np.array(${_mat2py(C)})\n`;
    code += `    D = np.array(${_mat2py(D)})\n\n`;
    code += `    def __init__(self):\n`;
    code += `        self.x = np.zeros(${n})\n\n`;
    code += `    def step(self, u):\n`;
    code += `        u = np.atleast_1d(u)\n`;
    code += `        y = self.C @ self.x + self.D @ u\n`;
    code += `        self.x = self.A @ self.x + self.B @ u\n`;
    code += `        return y\n`;
  } else {
    // matlab
    code += `% State-space controller\n`;
    code += `A = [${A.map((r) => r.map(_fmt).join(' ')).join('; ')}];\n`;
    code += `B = [${B.map((r) => r.map(_fmt).join(' ')).join('; ')}];\n`;
    code += `C = [${C.map((r) => r.map(_fmt).join(' ')).join('; ')}];\n`;
    code += `D = [${D.map((r) => r.map(_fmt).join(' ')).join('; ')}];\n`;
    if (discrete) {
      code += `${varName} = ss(A, B, C, D, ${Ts_});\n`;
    } else {
      code += `${varName} = ss(A, B, C, D);\n`;
    }
  }

  return { code, language: lang, warnings };
}

// ── State-feedback gain ───────────────────────────────────────────────────────

function _exportSF(ctrl, lang, { varName, Ts, header, warnings }) {
  const K = ctrl.K ?? [];
  const n = K.length;

  let code = header ? _fileHdr(lang, `State-feedback gain K=[${_arr(K)}]`) : '';

  if (lang === 'c') {
    code += `static const double ${varName}_K[${n}] = {${_arr(K)}};\n\n`;
    code += `double ${varName}_step(const double x[${n}]) {\n`;
    code += `  double u = 0;\n`;
    code += `  for (int i = 0; i < ${n}; i++) u -= ${varName}_K[i] * x[i];\n`;
    code += `  return u;\n}\n`;
  } else if (lang === 'python') {
    code += `import numpy as np\n`;
    code += `K_${varName} = np.array([${_arr(K)}])\n\n`;
    code += `def ${_pyName(varName)}_step(x):\n`;
    code += `    return float(-K_${varName} @ np.atleast_1d(x))\n`;
  } else {
    code += `K = [${_arr(K)}];\n`;
    code += `u = -K * x;  %% state-feedback: u = -Kx\n`;
  }

  return { code, language: lang, warnings };
}

// ── util ──────────────────────────────────────────────────────────────────────

function _padLeft(arr, n) {
  const out = new Array(n).fill(0);
  const offset = n - arr.length;
  for (let i = 0; i < arr.length; i++) out[offset + i] = arr[i];
  return out;
}

function _pyName(s) {
  return s.charAt(0).toUpperCase() + s.slice(1);
}

function _mat2py(mat) {
  return '[' + mat.map((row) => '[' + row.map(_fmt).join(', ') + ']').join(', ') + ']';
}


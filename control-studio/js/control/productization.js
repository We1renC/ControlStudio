/**
 * Generate an HTML design report from analysis results.
 *
 * The `design` object can contain any subset of:
 *   title       — string
 *   description — string
 *   margins     — { phaseMargin, gainMargin, gainCrossover, phaseFreq }
 *   poles       — number[][]  [[re,im], ...]
 *   zeros       — number[][]
 *   stepResponse — { time: number[], output: number[], overshoot?, settlingTime?, riseTime? }
 *   params      — { [key]: value }  arbitrary param table
 *   sections    — [{ title, content }]  freeform HTML sections
 *   warnings    — string[]
 *
 * @param {object}  design
 * @param {object}  [opts]
 * @param {string}  [opts.theme='light']  'light' | 'dark'
 * @param {boolean} [opts.includeCSS=true]
 * @returns {{ html: string, sections: string[] }}
 */
export function generateDesignReport(design, opts = {}) {
  const title  = design.title ?? 'Control Design Report';
  const theme  = opts.theme ?? 'light';
  const css    = opts.includeCSS !== false;
  const date   = new Date().toISOString().slice(0, 10);
  const sects  = [];

  const bg  = theme === 'dark' ? '#1a1a2e' : '#ffffff';
  const fg  = theme === 'dark' ? '#e0e0e0' : '#1a1a2e';
  const acc = theme === 'dark' ? '#4ade80' : '#1d6f42';
  const tbl = theme === 'dark' ? '#2a2a3e' : '#f0f4f8';

  let html = `<!DOCTYPE html>\n<html lang="en">\n<head>\n<meta charset="UTF-8">\n`;
  html += `<meta name="viewport" content="width=device-width, initial-scale=1">\n`;
  html += `<title>${_esc(title)}</title>\n`;

  if (css) {
    html += `<style>\n`;
    html += `body{background:${bg};color:${fg};font-family:system-ui,sans-serif;`;
    html += `max-width:900px;margin:0 auto;padding:2rem;line-height:1.6}\n`;
    html += `h1{color:${acc};border-bottom:2px solid ${acc};padding-bottom:.4rem}\n`;
    html += `h2{color:${acc};margin-top:2rem}\n`;
    html += `table{border-collapse:collapse;width:100%;margin:.8rem 0}\n`;
    html += `th,td{padding:.5rem 1rem;text-align:left;border:1px solid ${acc}30}\n`;
    html += `th{background:${acc}22;font-weight:600}\n`;
    html += `tr:nth-child(even){background:${tbl}}\n`;
    html += `.pass{color:#4ade80}.fail{color:#f87171}.warn{color:#fbbf24}\n`;
    html += `.badge{display:inline-block;padding:.15rem .5rem;border-radius:.25rem;`;
    html += `font-size:.8rem;font-weight:700}\n`;
    html += `.badge-ok{background:#16a34a22;color:#4ade80}`;
    html += `.badge-warn{background:#92400e22;color:#fbbf24}`;
    html += `.badge-fail{background:#7f1d1d22;color:#f87171}\n`;
    html += `pre{background:${tbl};padding:1rem;border-radius:.5rem;overflow-x:auto;font-size:.85rem}\n`;
    html += `footer{margin-top:3rem;font-size:.8rem;color:${fg}66;text-align:center}\n`;
    html += `</style>\n`;
  }

  html += `</head>\n<body>\n`;
  html += `<h1>${_esc(title)}</h1>\n`;
  html += `<p><strong>Generated:</strong> ${date}</p>\n`;

  if (design.description) {
    html += `<p>${_esc(design.description)}</p>\n`;
    sects.push('description');
  }

  // ── Stability Margins ────────────────────────────────────────────────────
  if (design.margins) {
    const m  = design.margins;
    const pm = Number.isFinite(m.phaseMargin)  ? m.phaseMargin.toFixed(2)  : '—';
    const gm = Number.isFinite(m.gainMargin)   ? m.gainMargin.toFixed(2)   : '—';
    const wc = Number.isFinite(m.gainCrossover) ? m.gainCrossover.toFixed(4) : '—';
    const wp = Number.isFinite(m.phaseFreq)    ? m.phaseFreq.toFixed(4)    : '—';
    const pmOk = Number(pm) >= 30;
    const gmOk = Number(gm) >= 6;
    html += `<h2>Stability Margins</h2>\n`;
    html += `<table><thead><tr><th>Metric</th><th>Value</th><th>Status</th></tr></thead><tbody>\n`;
    html += `<tr><td>Phase Margin</td><td>${pm}°</td><td>${_badge(pmOk ? 'ok' : 'warn', pm + '°')}</td></tr>\n`;
    html += `<tr><td>Gain Margin</td><td>${gm} dB</td><td>${_badge(gmOk ? 'ok' : 'warn', gm + ' dB')}</td></tr>\n`;
    html += `<tr><td>Gain Crossover ω<sub>c</sub></td><td>${wc} rad/s</td><td>—</td></tr>\n`;
    html += `<tr><td>Phase Crossover ω<sub>π</sub></td><td>${wp} rad/s</td><td>—</td></tr>\n`;
    html += `</tbody></table>\n`;
    sects.push('margins');
  }

  // ── Poles & Zeros ────────────────────────────────────────────────────────
  if (design.poles?.length) {
    html += `<h2>Closed-Loop Poles</h2>\n`;
    html += `<table><thead><tr><th>#</th><th>Real</th><th>Imag</th><th>Stable</th></tr></thead><tbody>\n`;
    design.poles.forEach(([re, im], i) => {
      const stable = re < 0;
      html += `<tr><td>${i + 1}</td><td>${_fmt(re)}</td><td>${_fmt(im)}</td>`;
      html += `<td>${_badge(stable ? 'ok' : 'fail', stable ? 'STABLE' : 'UNSTABLE')}</td></tr>\n`;
    });
    html += `</tbody></table>\n`;
    sects.push('poles');
  }

  if (design.zeros?.length) {
    html += `<h2>Open-Loop Zeros</h2>\n`;
    html += `<table><thead><tr><th>#</th><th>Real</th><th>Imag</th></tr></thead><tbody>\n`;
    design.zeros.forEach(([re, im], i) => {
      html += `<tr><td>${i + 1}</td><td>${_fmt(re)}</td><td>${_fmt(im)}</td></tr>\n`;
    });
    html += `</tbody></table>\n`;
    sects.push('zeros');
  }

  // ── Step Response Metrics ────────────────────────────────────────────────
  if (design.stepResponse) {
    const sr = design.stepResponse;
    html += `<h2>Step Response Metrics</h2>\n`;
    html += `<table><thead><tr><th>Metric</th><th>Value</th></tr></thead><tbody>\n`;
    if (Number.isFinite(sr.overshoot))
      html += `<tr><td>Overshoot</td><td>${sr.overshoot.toFixed(2)}%</td></tr>\n`;
    if (Number.isFinite(sr.settlingTime))
      html += `<tr><td>Settling Time (2%)</td><td>${sr.settlingTime.toFixed(4)} s</td></tr>\n`;
    if (Number.isFinite(sr.riseTime))
      html += `<tr><td>Rise Time (10→90%)</td><td>${sr.riseTime.toFixed(4)} s</td></tr>\n`;
    if (Number.isFinite(sr.steadyState))
      html += `<tr><td>Steady-State</td><td>${sr.steadyState.toFixed(6)}</td></tr>\n`;
    html += `</tbody></table>\n`;

    // ASCII sparkline
    if (Array.isArray(sr.time) && Array.isArray(sr.output) && sr.time.length > 0) {
      html += `<details><summary>Step Response Data (${sr.time.length} points)</summary>\n<pre>`;
      const COLS = 60, ROWS = 12;
      const tMax = sr.time[sr.time.length - 1];
      const yMax = Math.max(...sr.output, 1e-9);
      const yMin = Math.min(...sr.output, 0);
      const yRange = yMax - yMin || 1;
      // Reduce to COLS points
      const step = Math.max(1, Math.floor(sr.time.length / COLS));
      const grid = Array.from({ length: ROWS }, () => Array(COLS).fill(' '));
      for (let i = 0; i < COLS; i++) {
        const idx = Math.min(i * step, sr.output.length - 1);
        const row = ROWS - 1 - Math.round(((sr.output[idx] - yMin) / yRange) * (ROWS - 1));
        const clampedRow = Math.max(0, Math.min(ROWS - 1, row));
        grid[clampedRow][i] = '█';
      }
      // y-axis labels
      html += `y=${yMax.toFixed(3)} |`;
      grid.forEach((row, ri) => {
        html += row.join('');
        if (ri < ROWS - 1) {
          const yVal = yMax - (ri + 1) * yRange / ROWS;
          html += `\n${ri === Math.floor(ROWS / 2) ? 'y=' + yVal.toFixed(3) + ' |' : '       |'}`;
        }
      });
      html += `\n       +${'-'.repeat(COLS)} t → ${tMax.toFixed(3)}s\n</pre></details>\n`;
    }
    sects.push('stepResponse');
  }

  // ── Arbitrary params table ───────────────────────────────────────────────
  if (design.params && Object.keys(design.params).length > 0) {
    html += `<h2>Design Parameters</h2>\n`;
    html += `<table><thead><tr><th>Parameter</th><th>Value</th></tr></thead><tbody>\n`;
    for (const [k, v] of Object.entries(design.params)) {
      html += `<tr><td>${_esc(k)}</td><td>${_esc(String(v))}</td></tr>\n`;
    }
    html += `</tbody></table>\n`;
    sects.push('params');
  }

  // ── Freeform sections ────────────────────────────────────────────────────
  if (Array.isArray(design.sections)) {
    for (const sec of design.sections) {
      html += `<h2>${_esc(sec.title ?? '')}</h2>\n${sec.content ?? ''}\n`;
      sects.push(sec.title ?? 'section');
    }
  }

  // ── Warnings ─────────────────────────────────────────────────────────────
  if (design.warnings?.length) {
    html += `<h2>Warnings</h2>\n<ul>\n`;
    for (const w of design.warnings) {
      html += `  <li class="warn">Warning: ${_esc(w)}</li>\n`;
    }
    html += `</ul>\n`;
    sects.push('warnings');
  }

  html += `<footer>ControlStudio P33 — Auto-generated report</footer>\n`;
  html += `</body>\n</html>\n`;

  return { html, sections: sects };
}

function _badge(kind, text) {
  return `<span class="badge badge-${kind}">${_esc(String(text))}</span>`;
}

function _esc(s) {
  return String(s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}


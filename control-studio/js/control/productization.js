/**
 * Serialize a ControlStudio object to a JSON structure compatible with
 * python-control's to_dict / from_dict convention.
 *
 * Supported input shapes:
 *   { type:'tf',  num, den, Ts? }
 *   { type:'ss',  A, B, C, D, Ts? }
 *   { type:'pid', Kp, Ki, Kd, N?, Ts? }  → converted to TF first
 *
 * python-control convention (StateSpace.to_dict):
 *   { type: 'StateSpace', A, B, C, D, dt }   dt=0 for continuous
 *   { type: 'TransferFunction', num, den, dt }
 *
 * @param {object}  obj  ControlStudio controller/model descriptor.
 * @returns {object}  Plain JSON-serializable object.
 */
export function toPythonControl(obj) {
  if (!obj || typeof obj !== 'object') throw new Error('obj must be an object');
  const type = (obj.type || '').toLowerCase();
  const dt   = obj.Ts ?? 0;

  if (type === 'tf') {
    return {
      type: 'TransferFunction',
      num:  [obj.num ?? [1]],   // python-control wraps each channel: num[i][j] = row i, col j
      den:  [obj.den ?? [1]],
      dt,
    };
  }

  if (type === 'ss') {
    if (!obj.A || !obj.B || !obj.C || !obj.D) throw new Error('SS requires A,B,C,D');
    return {
      type: 'StateSpace',
      A: obj.A,
      B: obj.B,
      C: obj.C,
      D: obj.D,
      dt,
    };
  }

  if (type === 'pid') {
    // Convert PID to TF, then serialize
    const { Kp = 1, Ki = 0, Kd = 0, N = 100 } = obj;
    // C(s) numerator/denominator in continuous time
    // = [Kd*N + Kp, Kp*N + Ki, Ki*N] / [1, N, 0]
    const num = [Kd * N + Kp, Kp * N + Ki, Ki * N];
    const den = [1, N, 0];
    return {
      type:     'TransferFunction',
      num:      [num],
      den:      [den],
      dt,
      _source:  { type: 'PID', Kp, Ki, Kd, N },
    };
  }

  throw new Error(`toPythonControl: unsupported type "${obj.type}"`);
}

/**
 * Deserialize a python-control JSON dict back to a ControlStudio descriptor.
 *
 * @param {object}  data  JSON from python-control to_dict / from toPythonControl.
 * @returns {object}  ControlStudio descriptor { type, ..., Ts? }
 */
export function fromPythonControl(data) {
  if (!data || typeof data !== 'object') throw new Error('data must be an object');
  const type = data.type ?? '';
  const Ts   = (data.dt && data.dt !== 0) ? data.dt : undefined;

  if (type === 'TransferFunction') {
    // python-control stores num/den as nested arrays [[coeffs]]
    const num = Array.isArray(data.num?.[0]) ? data.num[0] : (data.num ?? [1]);
    const den = Array.isArray(data.den?.[0]) ? data.den[0] : (data.den ?? [1]);
    const out = { type: 'tf', num, den };
    if (Ts !== undefined) out.Ts = Ts;
    if (data._source) {
      // Recover original PID if available
      const s = data._source;
      if (s.type === 'PID') return { type: 'pid', Kp: s.Kp, Ki: s.Ki, Kd: s.Kd, N: s.N, ...(Ts !== undefined ? { Ts } : {}) };
    }
    return out;
  }

  if (type === 'StateSpace') {
    if (!data.A || !data.B || !data.C || !data.D) throw new Error('StateSpace requires A,B,C,D');
    const out = { type: 'ss', A: data.A, B: data.B, C: data.C, D: data.D };
    if (Ts !== undefined) out.Ts = Ts;
    return out;
  }

  throw new Error(`fromPythonControl: unsupported type "${type}"`);
}


/**
 * Design Wizard — spec-driven controller architecture recommender.
 *
 * Takes a performance specification and returns:
 *   - Recommended controller type & order
 *   - Ordered design workflow steps
 *   - Estimated design complexity
 *   - Relevant ControlStudio API calls with parameters
 *
 * @param {object}  spec
 * @param {number}  [spec.overshoot]        Max % overshoot (e.g. 10)
 * @param {number}  [spec.settlingTime]     Settling time in seconds
 * @param {number}  [spec.bandwidth]        Target bandwidth in rad/s
 * @param {number}  [spec.phaseMargin]      Min phase margin in degrees (default 45)
 * @param {number}  [spec.gainMargin]       Min gain margin in dB (default 6)
 * @param {boolean} [spec.robustness]       True if parametric uncertainty matters
 * @param {boolean} [spec.nonlinear]        True for nonlinear plant
 * @param {boolean} [spec.safety]           True if hard safety constraints needed
 * @param {boolean} [spec.adaptive]         True if plant varies / unknown
 * @param {boolean} [spec.discrete]         True for embedded digital implementation
 * @param {number}  [spec.Ts]              Sample time (s), implies discrete
 * @param {'siso'|'mimo'} [spec.topology]  Default 'siso'
 * @param {number}  [spec.nInputs]         Number of control inputs (MIMO)
 * @param {number}  [spec.nOutputs]        Number of plant outputs (MIMO)
 *
 * @returns {{
 *   recommendation: string,
 *   controllerType: string,
 *   workflow: { step:number, action:string, api:string, note:string }[],
 *   complexity: 'low'|'medium'|'high'|'very-high',
 *   warnings: string[],
 *   alternatives: string[],
 * }}
 */
export function designWizard(spec = {}) {
  const {
    overshoot      = null,
    settlingTime   = null,
    bandwidth      = null,
    phaseMargin    = 45,
    gainMargin     = 6,
    robustness     = false,
    nonlinear      = false,
    safety         = false,
    adaptive       = false,
    discrete       = !!(spec.Ts),
    Ts             = null,
    topology       = 'siso',
    nInputs        = 1,
    nOutputs       = 1,
  } = spec;

  const warnings     = [];
  const alternatives = [];
  const workflow     = [];
  let   step         = 1;

  // ── Validate ────────────────────────────────────────────────────────────
  if (overshoot !== null && (overshoot < 0 || overshoot > 100)) {
    warnings.push('overshoot should be between 0 and 100 (%).');
  }
  if (settlingTime !== null && settlingTime <= 0) {
    warnings.push('settlingTime should be positive (seconds).');
  }
  if (discrete && !Ts) {
    warnings.push('discrete=true but Ts not specified; provide Ts for quantisation analysis.');
  }
  if (topology === 'mimo' && (nInputs < 2 || nOutputs < 2)) {
    warnings.push('MIMO topology selected but nInputs/nOutputs < 2; check dimensions.');
  }

  // ── Decide controller type ───────────────────────────────────────────────
  let ctrlType = 'PID';
  let complexity = 'low';
  let rec = '';

  const isMIMO      = topology === 'mimo' || nInputs > 1 || nOutputs > 1;
  const tightSpec   = (overshoot !== null && overshoot < 5) ||
                      (phaseMargin > 60) ||
                      (bandwidth !== null && bandwidth > 100);
  const relaxedSpec = overshoot === null && settlingTime === null && bandwidth === null;

  if (safety) {
    ctrlType   = nonlinear ? 'CLF-CBF (Nonlinear)' : 'MPC with Constraints';
    complexity = 'high';
    rec        = 'Hard safety constraints require a QP-based safety filter (CLF-CBF) or constrained MPC.';
    alternatives.push('Tube MPC', 'LPV with saturations');
  } else if (nonlinear && adaptive) {
    ctrlType   = 'MRAC or STR';
    complexity = 'very-high';
    rec        = 'Nonlinear + adaptive plant is the hardest case; MRAC with Lyapunov rule is most principled.';
    alternatives.push('Gain-scheduling', 'Feedback Linearization + RLS');
  } else if (nonlinear) {
    ctrlType   = 'Feedback Linearization';
    complexity = 'high';
    rec        = 'Nonlinear plant with differentially-flat output: use exact I/O linearization; then apply linear outer loop.';
    alternatives.push('Backstepping', 'Sliding Mode Control', 'Gain scheduling');
  } else if (adaptive) {
    ctrlType   = 'MRAC';
    complexity = 'high';
    rec        = 'Time-varying or uncertain plant: MRAC (MIT / Lyapunov rule) provides automatic parameter tracking.';
    alternatives.push('STR', 'RLS + gain scheduling');
  } else if (isMIMO && robustness) {
    ctrlType   = 'H∞ (MIMO)';
    complexity = 'high';
    rec        = 'MIMO plant with robustness requirements: synthesize H∞ controller via γ-iteration or D-K iteration (μ-synthesis) for structured uncertainty.';
    alternatives.push('LQR/LQG', 'LPV synthesis', 'D-K iteration');
  } else if (isMIMO) {
    ctrlType   = 'LQR/LQG';
    complexity = 'medium';
    rec        = 'MIMO plant without specific robustness requirements: LQR for full state, LQG (LQR+Kalman) for output feedback.';
    alternatives.push('H∞', 'Decentralised PID', 'MPC');
  } else if (robustness) {
    ctrlType   = 'H∞ Loop-shaping';
    complexity = 'medium';
    rec        = 'SISO with robustness emphasis: H∞ loop-shaping gives explicit ‖S‖∞ / ‖T‖∞ bounds.';
    alternatives.push('QFT (not in suite)', 'Lead-lag + gain margin');
  } else if (tightSpec) {
    ctrlType   = 'Lead-Lag / State-feedback';
    complexity = 'medium';
    rec        = 'Tight transient spec with no uncertainty: pole-placement or lead compensator design is most direct.';
    alternatives.push('PID (auto-tuned)', 'LQR');
  } else {
    ctrlType   = 'PID';
    complexity = 'low';
    rec        = 'Standard SISO regulation: PID with Ziegler–Nichols or SIMC tuning is a proven starting point.';
    alternatives.push('Lead-Lag', 'IMC');
  }

  // ── Build workflow ───────────────────────────────────────────────────────
  // Step 1: always identify / model plant
  workflow.push({
    step: step++,
    action: 'Plant identification / modelling',
    api: 'identifyRLS(n, opts) or identifySRIVC(y, u, na, nb, Ts)',
    note: 'Obtain LTI model G(s) or G(z). Validate with frequency-response data.',
  });

  // Step 2: stability analysis
  workflow.push({
    step: step++,
    action: 'Open-loop stability & frequency analysis',
    api: 'stabilityMargins(plant)',
    note: `Confirm PM ≥ ${phaseMargin}°, GM ≥ ${gainMargin} dB. Check BIBO and pole locations.`,
  });

  // Type-specific steps
  if (ctrlType === 'PID') {
    workflow.push({
      step: step++,
      action: 'SIMC/IMC auto-tuning',
      api: `designIMC(plant, { lambda: ${settlingTime ? (settlingTime * 0.1).toFixed(2) : 0.5} })`,
      note: 'Set filter time-constant λ to desired closed-loop bandwidth.',
    });
    workflow.push({
      step: step++,
      action: 'PID fine-tuning with GA (optional)',
      api: `gaTuner(plant, pidCost, { popSize:50, generations:100 })`,
      note: 'Optimise Kp/Ki/Kd for multi-objective (overshoot + settling + robustness).',
    });
  } else if (ctrlType.startsWith('Lead')) {
    if (overshoot !== null && settlingTime !== null) {
      workflow.push({
        step: step++,
        action: 'Map spec → target poles',
        api: `specsToTargetPoles({ overshoot:${overshoot}, settlingTime:${settlingTime} })`,
        note: 'Compute dominant pole ζ, ωn, target s = −σ ± jωd.',
      });
    }
    workflow.push({
      step: step++,
      action: 'Lead compensator design',
      api: `designLeadForPM(plant, { targetPM:${phaseMargin} })`,
      note: 'Lift phase margin; cascade lead if single stage insufficient.',
    });
  } else if (ctrlType === 'LQR/LQG') {
    workflow.push({
      step: step++,
      action: 'LQR state-feedback gain',
      api: `solveCARE(A, B, Q, R)  →  K`,
      note: 'Choose Q/R weights to balance performance vs. control effort.',
    });
    workflow.push({
      step: step++,
      action: 'Kalman filter observer',
      api: `kalmanFilter(A, C, Qn, Rn)`,
      note: 'Design observer for output-feedback; combine with LQR for LQG.',
    });
  } else if (ctrlType.startsWith('H∞')) {
    workflow.push({
      step: step++,
      action: 'H∞ synthesis via γ-iteration',
      api: `synthesizeHinf(P, { gamma: 1.2 })`,
      note: 'Start with γ=1.2×‖P‖∞; iterate down to γ_min.',
    });
    if (isMIMO && robustness) {
      workflow.push({
        step: step++,
        action: 'D-K iteration for μ-synthesis',
        api: `dkIteration(plant, { maxIterations:6 })`,
        note: 'Minimise μ upper bound for structured uncertainty; alternates K-step and D-step.',
      });
    }
  } else if (ctrlType === 'Feedback Linearization') {
    workflow.push({
      step: step++,
      action: 'Define nonlinear model f(x), g(x), h(x)',
      api: `feedbackLinearization(f, g, h, r, { k: [...] })`,
      note: 'Compute relative degree r; verify Lg·Lf^{r-1} h ≠ 0 (regularity).',
    });
    workflow.push({
      step: step++,
      action: 'Outer linear controller design',
      api: `specsToTargetPoles(...)  →  place k gains`,
      note: 'After exact linearization, design linear outer loop for tracking.',
    });
  } else if (ctrlType.startsWith('CLF-CBF') || ctrlType.startsWith('MPC')) {
    workflow.push({
      step: step++,
      action: 'Define Lyapunov function V(x) and barrier h(x)',
      api: `controlBarrierFunction(f, g, V, h, { gamma, alpha })`,
      note: 'Ensure h(x)≥0 is the safe set; choose α (class-K) for barrier tightness.',
    });
    if (ctrlType.startsWith('MPC')) {
      workflow.push({
        step: step++,
        action: 'Constrained MPC setup',
        api: `mpcController(model, { N:10, uMin, uMax, xMin, xMax })`,
        note: 'Horizon N≥5; add terminal cost / constraint for stability guarantee.',
      });
    }
  } else if (ctrlType.startsWith('MRAC')) {
    workflow.push({
      step: step++,
      action: 'Reference model selection',
      api: `designMRAC(refModel, { rule:'lyapunov', gammaR:0.5, gammaX:0.5 })`,
      note: 'Choose stable reference model matching desired closed-loop response.',
    });
  }

  // Discrete implementation (common last step)
  if (discrete || Ts) {
    workflow.push({
      step: step++,
      action: 'Discretize controller',
      api: `c2dTustin(ctrl, ${Ts ?? 0.01})`,
      note: `Sample at Ts=${Ts ?? '?'} s; bilinear (Tustin) preserves stability margin.`,
    });
  }

  // Validation step (always last)
  workflow.push({
    step: step++,
    action: 'Closed-loop simulation & validation',
    api: 'simulate(closedLoop, stepInput, { tEnd: 10 })',
    note: 'Verify overshoot, settling, and margin specs. Export report via generateDesignReport.',
  });

  return {
    recommendation: rec,
    controllerType: ctrlType,
    workflow,
    complexity,
    warnings,
    alternatives,
  };
}
